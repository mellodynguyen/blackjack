  def card_values(card):
    """adds value to the face cards and ace, keeps numbered cards = to the value they're assigned to"""
    if card[0] in ['Jack', 'Queen', 'King']:
      return 10
    elif card[0] == 'Ace':
      return 11
    else:
      return int(card[0])
  
